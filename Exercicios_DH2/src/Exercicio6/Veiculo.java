package Exercicio6;

public class Veiculo {

    public String marca;
    public String modelo;
    public int anoDeFrabricacao;
    public String cor;
    public int quilometragem;

    public Veiculo(String marca, String modelo, int anoDeFrabricacao, String cor, int quilometragem) {
        this.marca = marca;
        this.modelo = modelo;
        this.anoDeFrabricacao = anoDeFrabricacao;
        this.cor = cor;
        this.quilometragem = quilometragem;
    }
}
